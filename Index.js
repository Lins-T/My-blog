function Join() {
			var currentMenu = document.getElementById("Joinlink");
			
			Joinlink.style.bottom = "-100%"
			
			if(currentMenu == "-100%") {
			
							Joinlink.style.bottom = "0";
			}
			
			
			else {
				Joinlink.style.bottom = "0";			
			}
}



function quit() {
				var currentMenu = document.getElementById("Joinlink")	;
			
			Joinlink.style.bottom = "0";
			
			if(currentMenu = "0") {
							Joinlink.style.bottom = "-100%";
			}
			else {
				Joinlink.style.bottom = "0";			
			}
}

